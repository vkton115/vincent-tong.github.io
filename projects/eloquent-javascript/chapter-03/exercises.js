// min /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function min(num1, num2) {
  return Math.min(num1, num2)
}

////////////////////////////////////////////////////////////////////////////////
// isEven //////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function isEven(num) {
  if (num % 2 === 0){
    return true;
  } else if (num % 2 === 1){
    return false;
  } else {
    return false;
  }
}

////////////////////////////////////////////////////////////////////////////////
// countChars //////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countChars(string, character) {
  strArr = [];
  for (let i = 0; i < string.length; i++){
    if (character == string[i]){
      strArr.push(string[i]);
    }
  } return strArr.length;

}

////////////////////////////////////////////////////////////////////////////////
// countBs /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function countBs(string) {
  count = 0;
  for (let i = 0; i < string.length; i++){
    if (string[i] === "B"){
      count = count + 1;
    }
  } return count;
}

