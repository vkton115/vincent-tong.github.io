// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
  //output array
  var range = [];
  //edge case
  if (start === end){
    return [];
  }
  if (step === undefined){
    for (let i = start; i <= end; i++){
      range.push(i);
    }
    //if step is passed in
  } else {
    if (step < 0){
      return [];
    } else {
      for (let i = start; i <= end; i += step){
        range.push(i);
      }
    }
  }
  return range;
}

