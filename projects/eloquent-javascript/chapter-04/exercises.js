////////////////////////////////////////////////////////////////////////////////
// range ///////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function range(start, end, step) {
  //output array
  var range = [];
  //edge case
  if (start === end){
    return [];
  }
  if (step === undefined){
    for (let i = start; i <= end; i++){
      range.push(i);
    }
    //if step is passed in
  } else {
    if (step < 0){
      return [];
    } else {
      for (let i = start; i <= end; i += step){
        range.push(i);
      }
    }
  }
  return range;
}

////////////////////////////////////////////////////////////////////////////////
// sum /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function sum(array) {
  var sum = 0;
  for (let i = 0; i < array.length; i++){
    sum += array[i];
  }
  return sum;
}

////////////////////////////////////////////////////////////////////////////////
// reverseArray ////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArray(array) {
  //create output array
  var reversed = [];
  for (let i = array.length -1; i >= 0; i--){
    reversed.push(array[i]);
  }
  return reversed;
}

////////////////////////////////////////////////////////////////////////////////
// reverseArrayInPlace /////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function reverseArrayInPlace(array) {
  if (array.length % 2 === 0){
    for (let i = 0; i < array.length/2; i++){
      var temp = array[i];
      array[i] = array[array.length - 1 - i]
      array[array.length - 1 - i] = temp;
    }
  } else {
    //math.floor rounds it down because nothing needs to happen to the middle value
    for (let i = 0; i < Math.floor(array.length/2); i++){
      var temp = array[i];
      array[i] = array[array.length -1 - i];
      array[array.length - 1 - i] = temp;
    }
  }
  return array;
}

////////////////////////////////////////////////////////////////////////////////
// arrayToList /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function arrayToList(array) {

  var rest = null;

  for (var i = array.length -1; i >=0; i--){
    rest = { value: array[i], rest}
  }
  return rest;
}

////////////////////////////////////////////////////////////////////////////////
// listToArray /////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function listToArray(list) {
  var arr = [];
  for (let node = list; node; node = node.rest){
    arr.push(node.value);
  }
  return arr;
}

////////////////////////////////////////////////////////////////////////////////
// prepend /////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function prepend(element, list) {
  return {
    value: element,
    rest: list
  };
}
////////////////////////////////////////////////////////////////////////////////
// nth /////////////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function nth(list, n) {
  //base
  if (n === 0){
    return list.value;
  } else if (n < 0){
    return undefined;
  }
  //recursion
  return nth(list.rest, n - 1);
}

////////////////////////////////////////////////////////////////////////////////
// deepEqual ///////////////////////////////////////////////////////////////////
////////////////////////////////////////////////////////////////////////////////

function deepEqual(val1, val2) {
  //strictly compare to see if they are the same
  if (val1 === val2) return true;
  //test if either value is null or not an object
  if (val1 == null || typeof val1 != "object" ||
      val2 == null || typeof val2 != "object")
    return false;
  //declaring counters to count the amount of properties in each value
  var val1prop = 0, val2prop = 0;
  for (var prop in val1)
    val1prop += 1;

  for (var prop in val2) {
    val2prop += 1;

    //First condition: if a property val2 is 'falsy' or does not exist in val1, return false.
    //Second Condition: recursively tests if the properties of val1 aren't equal to val2.
    if (!(prop in val1) || !deepEqual(val1[prop], val2[prop]))
      return false;
  }
  return val1prop == val2prop;
}

